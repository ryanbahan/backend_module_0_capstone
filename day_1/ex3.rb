# Math symbols

# + plus // addition operator
# - minus // subtraction operator
# / slash // division operator
# * asterisk // multiplication operator
# % percent // modulo operator: produces remainder of quotient for 2 Numbers
# < less-than // checks if value of left operand is less than value of right operand
# > greater-than // checks if value of left operand is greater than value of right operand
# < less-than-equal // checks if value of left operand is less than or equal to value of right operand
# > greater-than-equal // checks if value of left operand is greater than or equal to value of right operand

# Prints the string in quotations
puts "I will now count my chickens:"

# Prints the string in quotations and calculates the expression in brackets
puts "Hens #{25.0 + 30 / 6}"
puts "Roosters #{100 - 25 * 3 % 4}"

# Prints the string in quotations
