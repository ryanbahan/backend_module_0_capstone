# Prints the string in quotations
puts "Is it true that 3 + 2 < 5 - 7?"

# Calculates the expression
puts 3.0 + 2 < 5 - 7

# Prints the string in quotations and calculates the expression in brackets
puts "What is 3 + 2? #{3.0 + 2}"
